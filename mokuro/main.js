function copyText() {
	const textField = textField.select();
	textField.setSelectionRange(0, 99999); // iOS compatibility
}

let loadButton = document.getElementById("load-text-button");
loadButton.addEventListener("click", loadText);

function loadText() {
	let numberInput = document.getElementById("number-input");
	let pageNumber = numberInput.value;

	let d = data[pageNumber];

	let bodyContainer = document.getElementById("body-container");
	bodyContainer.innerHTML = "";

	for (let i = 0; i < d.length; i++) {
		let newDiv = document.createElement("div");
		newDiv.className = "text-paragraph";
		newDiv.innerHTML = d[i];
		newDiv.onclick = function (event) {
			navigator.clipboard.writeText(event.target.innerHTML);
		};
		bodyContainer.appendChild(newDiv);
	}
}
